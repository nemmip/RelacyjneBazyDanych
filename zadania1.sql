------------------------------------------------------------------------------------------
---------- Wyświetl klientów którzy są z miasta Lulea, London lub Warszawa.
------------------------------------------------------------------------------------------
---------- Wyświetl klientów którzy są z któregoś z miast: Lulea, London, Warszawa.
------------------------------------------------------------------------------------------
---------- IN
------------------------------------------------------------------------------------------
---------- 8 rows
------------------------------------------------------------------------------------------
select *
from Customers where City='Lulea' or City='London' or City='Warszawa'

---------- lub
select *
from Customers where City in ('Lulea', 'London', 'Warszawa')




------------------------------------------------------------------------------------------
---------- Wyświetl nazwisko, imię i miasto pracownika. 
---------- W kolejności alfabetycznej względem trzeciej i pierwszej kolumny.
