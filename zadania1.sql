------------------------------------------------------------------------------------------
---------- 67 rows
------------------------------------------------------------------------------------------
select *
from Products where UnitPrice<100



------------------------------------------------------------------------------------------
---------- Wyświetl produkty w cenie pomiędzy 11 a 13.
------------------------------------------------------------------------------------------
---------- Wyświetl produkty droższe niż 11 a tańsze niż 13.
------------------------------------------------------------------------------------------
---------- 5 rows
------------------------------------------------------------------------------------------
select *
from Products where UnitPrice between 11 and 13

---------- lub
select *
from Products where UnitPrice>=11 and UnitPrice<=13




------------------------------------------------------------------------------------------
---------- Wyświetl produkty których cena (z podatkiem VAT) nie jest pomiędzy 49 a 99.
------------------------------------------------------------------------------------------
---------- 68 rows
------------------------------------------------------------------------------------------
select *
from Products where UnitPrice+Products.UnitPrice*0.23 not between 49 and 99

---------- lub
select *
from Products where UnitPrice+Products.UnitPrice*0.23<=49 or UnitPrice+Products.UnitPrice*0.23>=99



------------------------------------------------------------------------------------------
---------- Wyświetl produkty których nie ma w zapasie.
------------------------------------------------------------------------------------------
---------- 5 rows
------------------------------------------------------------------------------------------
select *
from Products where UnitsInStock=0



------------------------------------------------------------------------------------------
---------- Wyświetl produkty których nazwa zaczyna się na literę "T" 
---------- i jest nie dłuższa niż 5 znaków.
------------------------------------------------------------------------------------------
---------- LIKE
---------- _ - zastepuje dokładnie jeden znak
------------------------------------------------------------------------------------------
---------- 1 row
------------------------------------------------------------------------------------------
select *
from Products where ProductName like 'T___'



------------------------------------------------------------------------------------------
---------- Wyświetl produkty których nazwa zaczyna się na literę "S".
------------------------------------------------------------------------------------------
---------- % - zastępuje dowolny ciąg znaków również pusty
------------------------------------------------------------------------------------------
---------- 9 rows
------------------------------------------------------------------------------------------
select *
from Products where ProductName like 'S%'



------------------------------------------------------------------------------------------
---------- Wyświetl produkty których nazwa zaczyna się na literę z pierwszej połowy 
---------- alfabetu. Nazwy produktów wyświetl w kolejności alfabetycznej.
------------------------------------------------------------------------------------------
---------- 36 rows
------------------------------------------------------------------------------------------
select *
from Products where ProductName like '[A-L]%'
order by ProductName asc



------------------------------------------------------------------------------------------
---------- Wyświetl produkty których nazwa zaczyna się na literę z drugiej połowy 
---------- alfabetu. Nazwy produktów wyświetl w kolejności odwrotnej do alfabetycznej. 
------------------------------------------------------------------------------------------
---------- 36 rows
------------------------------------------------------------------------------------------
select *
from Products where ProductName like '[N-Z]%'
order by ProductName desc



